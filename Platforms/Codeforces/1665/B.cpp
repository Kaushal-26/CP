void solve() {
        int n; cin >> n;

        map<int, int> f;
        for(int i = 0, x; i < n; i++) {
                cin >> x;
                f[x]++;
        }

        int mx = 0;
        for(auto &[x, y] : f) mx = max(mx, y);

        int ans = 0;
        while(mx < n) {
                ans++;
                int need = min(n - mx, mx);
                mx += need;
                ans += need;
        }

        cout << ans << '\n';
}
 
